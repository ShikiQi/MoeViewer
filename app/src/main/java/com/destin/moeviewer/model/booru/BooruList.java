package com.destin.moeviewer.model.booru;

import org.simpleframework.xml.Attribute;
import org.simpleframework.xml.ElementList;
import org.simpleframework.xml.Root;

import java.util.List;

@Root(name = "posts")
public class BooruList {
    @ElementList(inline = true)
    private List<BooruPost> posts;
    @Attribute
    private int count;
    @Attribute
    private int offset;

    public List<BooruPost> getPosts() {
        return posts;
    }

    public void setPosts(List<BooruPost> posts) {
        this.posts = posts;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getOffset() {
        return offset;
    }

    public void setOffset(int offset) {
        this.offset = offset;
    }
}
